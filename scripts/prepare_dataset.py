#!/usr/bin/env python3
"""Prepare a Label Studio "YOLO with images" export for Ultralytics training.

Unpacks the export .zip, splits images/labels into train/val/test
(70/20/10, seeded), lays out the folders Ultralytics expects, and writes
data.yaml. Single class: crack. The export holds images/ (*.jpg|png),
labels/ (*.txt YOLO boxes), classes.txt and notes.json (both ignored).

Example:
    python prepare_dataset.py export.zip --out ../dataset --seed 42
"""
import argparse
import random
import shutil
import zipfile
from pathlib import Path

# Image extensions Label Studio may export.
IMG_EXTS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}
SPLITS = {"train": 0.70, "val": 0.20, "test": 0.10}


def unzip(src_zip: Path, work: Path) -> Path:
    """Extract the export zip and return the dir that holds images/labels."""
    work.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(src_zip) as zf:
        zf.extractall(work)
    # The images/ folder can sit at the root or one level down.
    for cand in [work, *[p for p in work.iterdir() if p.is_dir()]]:
        if (cand / "images").is_dir() and (cand / "labels").is_dir():
            return cand
    raise SystemExit("Could not find images/ and labels/ in the export.")


def pair_files(root: Path):
    """Return [(image_path, label_path_or_None), ...] for every image."""
    pairs = []
    for img in sorted((root / "images").iterdir()):
        if img.suffix.lower() not in IMG_EXTS:
            continue
        label = root / "labels" / (img.stem + ".txt")
        pairs.append((img, label if label.exists() else None))
    return pairs


def split_pairs(pairs, seed: int):
    """Deterministically split pairs into train/val/test buckets."""
    rng = random.Random(seed)
    shuffled = pairs[:]
    rng.shuffle(shuffled)
    n = len(shuffled)
    n_train = int(n * SPLITS["train"])
    n_val = int(n * SPLITS["val"])
    return {
        "train": shuffled[:n_train],
        "val": shuffled[n_train:n_train + n_val],
        "test": shuffled[n_train + n_val:],
    }


def write_split(out: Path, split: str, pairs):
    """Copy images/labels into out/<split>/images and out/<split>/labels."""
    img_dir = out / split / "images"
    lbl_dir = out / split / "labels"
    img_dir.mkdir(parents=True, exist_ok=True)
    lbl_dir.mkdir(parents=True, exist_ok=True)
    for img, label in pairs:
        shutil.copy2(img, img_dir / img.name)
        if label is not None:
            shutil.copy2(label, lbl_dir / label.name)


def write_yaml(out: Path):
    """Write the Ultralytics data.yaml describing the single 'crack' class."""
    yaml = (
        f"# Auto-generated by prepare_dataset.py\n"
        f"path: {out.resolve().as_posix()}\n"
        f"train: train/images\n"
        f"val: val/images\n"
        f"test: test/images\n\n"
        f"nc: 1\n"
        f"names:\n  0: crack\n"
    )
    (out / "data.yaml").write_text(yaml, encoding="utf-8")


def print_summary(splits):
    """Print a small table of image / labelled counts per split."""
    print(f"\n{'Split':<8}{'Images':>8}{'Labelled':>10}")
    print("-" * 26)
    total = labelled = 0
    for name in ("train", "val", "test"):
        pairs = splits[name]
        n_lab = sum(1 for _, lab in pairs if lab is not None)
        total += len(pairs)
        labelled += n_lab
        print(f"{name:<8}{len(pairs):>8}{n_lab:>10}")
    print("-" * 26)
    print(f"{'total':<8}{total:>8}{labelled:>10}\n")


def main():
    ap = argparse.ArgumentParser(description="Prepare Label Studio YOLO export for Ultralytics.")
    ap.add_argument("export_zip", type=Path, help="Label Studio 'YOLO with images' export .zip")
    ap.add_argument("--out", type=Path, default=Path("dataset"), help="Output dataset dir")
    ap.add_argument("--seed", type=int, default=42, help="Split RNG seed")
    ap.add_argument("--tmp", type=Path, default=Path(".ls_extract"), help="Scratch extract dir")
    args = ap.parse_args()

    if args.out.exists():
        shutil.rmtree(args.out)
    if args.tmp.exists():
        shutil.rmtree(args.tmp)

    root = unzip(args.export_zip, args.tmp)
    pairs = pair_files(root)
    if not pairs:
        raise SystemExit("No images found in export.")

    splits = split_pairs(pairs, args.seed)
    for name, bucket in splits.items():
        write_split(args.out, name, bucket)
    write_yaml(args.out)

    shutil.rmtree(args.tmp, ignore_errors=True)
    print_summary(splits)
    print(f"data.yaml -> {(args.out / 'data.yaml').resolve()}")


if __name__ == "__main__":
    main()
